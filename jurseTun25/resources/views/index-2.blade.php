                    <p>
                        The <a class="text-decoration-underline" href="http://jurse.org/" target="_blank">
                            <b>Joint Urban Remote Sensing Event (JURSE)</b></a> serves as a distinguished platform for the exchange of insights among researchers, practitioners, and students 
                             showcasing their most recent discoveries and achievements.<br>
                              The forthcoming edition promises to be exceptionally vibrant as it will be hosted at the Ramada Plaza Hotel in Tunis, Tunisia, from May 5 to 7, 2025.</p>
                    </p>

                    <p>
                        <b>
                            The primary commitment of this event is to introduce cutting-edge methodologies and the latest technological resources utilized for the exploration of diverse facets of the urban environment <br>
                            leveraging both orbital and airborne remote sensing data.
                        </b>
                    </p>

                    <p>
                        Cutting-edge subjects such as innovative approaches to distinguish urban land cover and land use, intricate 3D modeling of urban structures, forecasting and evaluating the impact of both natural and human-induced hazards in urban environments, studies in urban social dynamics, urban ecology, urban climatology, and the exploration of data fusion, algorithms, and techniques for interpreting Earth Observation data were explored.
                    </p>

